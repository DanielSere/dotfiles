local autocmd = vim.api.nvim_create_autocmd

autocmd('TextYankPost', {
	callback =
	function()
		vim.highlight.on_yank { timeout = 500 }
	end,
})

autocmd('FileType', {
	pattern = { 'help', 'man' },
	command = 'nnoremap <buffer> q <cmd>quit<cr>'
})
